def augment_tools_list(response):
    """Append bridge meta-tools to a tools/list response from TD."""
    if (response
            and "result" in response
            and "tools" in response["result"]):
        response["result"]["tools"].extend(BRIDGE_TOOLS)
    return response


def bridge_only_tools_list(request_id):
    """Generate a tools/list response with only bridge meta-tools."""
    return {
        "jsonrpc": "2.0",
        "id": request_id,
        "result": {"tools": list(BRIDGE_TOOLS)},
    }


