def handle_get_td_status(state):
    """Handle the get_td_status meta-tool."""
    # Refresh process liveness
    alive = is_process_alive(state["td_pid"])
    if state["td_pid"] and not alive:
        state["crash_detected"] = True

    last_ts = None
    if state["last_connected_time"] is not None:
        last_ts = time.strftime(
            "%Y-%m-%dT%H:%M:%SZ", time.gmtime(state["last_connected_time"]))

    remaining = CRASH_LOOP_MAX - len([
        t for t in state["launch_timestamps"]
        if t > time.monotonic() - CRASH_LOOP_WINDOW_S
    ])

    return {
        "connected": state["connected"],
        "td_process_alive": alive,
        "crash_detected": state["crash_detected"],
        "last_connected": last_ts,
        "toe_path": state["config"].get("toe_path", ""),
        "td_executable": state["config"].get("td_executable", ""),
        "restart_attempts_remaining": max(0, remaining),
    }


def handle_launch_td(params, state):
    """Handle the launch_td meta-tool."""
    timeout = params.get("timeout", 120)

    # Safety: check if TD is already running
    existing_pid = find_td_pid()
    if existing_pid and is_process_alive(existing_pid):
        return {
            "status": "error",
            "message": (
                f"TouchDesigner is already running (PID {existing_pid}). "
                "Close it first or use get_td_status to check its state."
            ),
        }

    # Crash-loop guard
    now = time.monotonic()
    recent = [t for t in state["launch_timestamps"]
              if t > now - CRASH_LOOP_WINDOW_S]
    if len(recent) >= CRASH_LOOP_MAX:
        return {
            "status": "error",
            "message": (
                f"TouchDesigner has crashed {CRASH_LOOP_MAX} times in the "
                f"last {CRASH_LOOP_WINDOW_S // 60} minutes. Something may be "
                "wrong with the project. Ask the user to investigate."
            ),
        }

    # Launch
    success, message, pid = launch_td(state["config"], state["config_path"])
    if not success:
        return {"status": "error", "message": message}

    state["td_pid"] = pid
    state["crash_detected"] = False
    state["launch_timestamps"].append(time.monotonic())
    log(message)

    # Wait for Envoy to become reachable
    url = state["url"]
    deadline = time.monotonic() + timeout
    if wait_for_envoy(url, deadline):
        state["connected"] = True
        state["last_connected_time"] = time.time()
        log("Envoy is reachable after TD launch")
        return {
            "status": "success",
            "message": f"TouchDesigner launched and Envoy is ready. PID: {pid}",
        }
    else:
        return {
            "status": "partial",
            "message": (
                f"TouchDesigner launched (PID {pid}) but Envoy not reachable "
                f"after {timeout}s. TD may still be loading — Embody needs "
                "~75 frames to complete restoration."
            ),
        }


def handle_restart_td(params, state):
    """Handle the restart_td meta-tool."""
    timeout = params.get("timeout", 120)

    # Find and quit the running TD process
    pid = find_td_pid()
    if not pid or not is_process_alive(pid):
        return {
            "status": "error",
            "message": (
                "TouchDesigner is not running. Use launch_td to start it."
            ),
        }

    log(f"Restarting TouchDesigner (PID {pid})")
    success, quit_msg = quit_td(pid)
    log(quit_msg)
    if not success:
        return {"status": "error", "message": quit_msg}

    # Clear connection state
    state["connected"] = False
    state["td_pid"] = None
    state["crash_detected"] = False

    # Launch fresh
    success, launch_msg, new_pid = launch_td(state["config"],
                                              state["config_path"])
    if not success:
        return {"status": "error", "message": launch_msg}

    state["td_pid"] = new_pid
    state["launch_timestamps"].append(time.monotonic())
    log(launch_msg)

    # Wait for Envoy to become reachable
    url = state["url"]
    deadline = time.monotonic() + timeout
    if wait_for_envoy(url, deadline):
        state["connected"] = True
        state["last_connected_time"] = time.time()
        log("Envoy is reachable after TD restart")
        return {
            "status": "success",
            "message": (
                f"TouchDesigner restarted and Envoy is ready. "
                f"Old PID: {pid}, New PID: {new_pid}"
            ),
        }
    else:
        return {
            "status": "partial",
            "message": (
                f"TouchDesigner relaunched (PID {new_pid}) but Envoy not "
                f"reachable after {timeout}s. TD may still be loading — "
                "Embody needs ~75 frames to complete restoration."
            ),
        }


def handle_bridge_tool(name, params, state):
    """Dispatch a bridge meta-tool call. Returns the tool result content."""
    if name == "get_td_status":
        result = handle_get_td_status(state)
    elif name == "launch_td":
        result = handle_launch_td(params, state)
    elif name == "restart_td":
        result = handle_restart_td(params, state)
    else:
        result = {"error": f"Unknown bridge tool: {name}"}

    return [{"type": "text", "text": json.dumps(result, indent=2)}]


