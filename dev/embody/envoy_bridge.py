def forward_to_http(url, message, timeout=300):
    """Forward a JSON-RPC message to the Envoy HTTP endpoint.

    Uses a fresh ``urllib.request.urlopen`` per call for simplicity
    and reliability.  Returns the parsed JSON-RPC response, or None
    for notifications that return no data.
    """
    data = json.dumps(message).encode("utf-8")
    req = urllib.request.Request(
        url,
        data=data,
        headers={
            "Content-Type": "application/json",
            "Accept": "application/json, text/event-stream",
        },
    )
    resp = urllib.request.urlopen(req, timeout=timeout)
    body = resp.read().decode("utf-8")

    # Streamable HTTP returns SSE format: "event: message\ndata: {...}\n\n"
    for line in body.split("\n"):
        stripped = line.strip()
        if stripped.startswith("data: "):
            try:
                return json.loads(stripped[6:])
            except json.JSONDecodeError:
                continue  # Truncated SSE line -- try next

    # Fallback: plain JSON response
    if body.strip():
        try:
            return json.loads(body)
        except json.JSONDecodeError:
            pass

    return None


def send_response(response):
    """Write a JSON-RPC response to stdout (STDIO MCP transport).

    Serialized via ``_stdout_lock`` so the reconciler thread's notification
    writes cannot interleave with a response mid-line.
    """
    with _stdout_lock:
        sys.stdout.write(json.dumps(response) + "\n")
        sys.stdout.flush()


def send_error(request_id, code, message):
    """Send a JSON-RPC error response."""
    send_response({
        "jsonrpc": "2.0",
        "id": request_id,
        "error": {"code": code, "message": message},
    })


def _get_parent_pid(pid):
    """Return the parent PID of a given process, or None on failure."""
    if sys.platform == "win32":
        try:
            ps_cmd = (
                f'(Get-CimInstance Win32_Process -Filter '
                f'"ProcessId = {pid}").ParentProcessId'
            )
            result = subprocess.run(
                ["powershell", "-NoProfile", "-Command", ps_cmd],
                capture_output=True, text=True, timeout=5,
            )
            val = result.stdout.strip()
            return int(val) if val.isdigit() else None
        except (subprocess.TimeoutExpired, FileNotFoundError, ValueError, OSError):
            return None
    # Linux: /proc/<pid>/stat field 4 is ppid
    try:
        stat_path = f"/proc/{pid}/stat"
        if os.path.exists(stat_path):
            with open(stat_path, "r") as f:
                fields = f.read().split()
            return int(fields[3]) if len(fields) > 3 else None
    except (OSError, ValueError, IndexError):
        pass
    # macOS: ps -p PID -o ppid=
    try:
        result = subprocess.run(
            ["ps", "-p", str(pid), "-o", "ppid="],
            capture_output=True, text=True, timeout=5,
        )
        val = result.stdout.strip()
        return int(val) if val else None
    except (subprocess.TimeoutExpired, FileNotFoundError, ValueError, OSError):
        return None


def _is_orphan(pid):
    """Return True if the given bridge process is an orphan (parent dead)."""
    ppid = _get_parent_pid(pid)
    if ppid is None:
        return True  # Can't determine -- assume orphan (safe to kill)
    if ppid <= 1:
        return True  # Reparented to init/launchd
    return not is_process_alive(ppid)


def kill_stale_bridges(port, config_path):
    """Find and terminate stale envoy-bridge processes.

    Phase 1: Kill bridges with stale heartbeat files (>HEARTBEAT_STALE_S old).
    This is the primary detection mechanism -- fast, reliable, no parent PID
    assumptions.

    Phase 2 (legacy fallback): For bridges that predate heartbeat files, fall
    back to pgrep/tasklist + _is_orphan() parent-PID checking.
    """
    my_pid = os.getpid()

    # --- Phase 1: Heartbeat-based detection ---
    stale = _list_stale_heartbeats(config_path, HEARTBEAT_STALE_S)
    for pid, age in stale:
        if pid == my_pid:
            continue
        try:
            if sys.platform == "win32":
                subprocess.run(
                    ["taskkill", "/F", "/PID", str(pid)],
                    capture_output=True, timeout=5,
                )
            else:
                os.kill(pid, signal.SIGTERM)
            log(f"Terminated stale bridge (PID {pid}, heartbeat {age:.0f}s old)")
        except (ProcessLookupError, PermissionError, OSError,
                subprocess.TimeoutExpired):
            pass

    # --- Phase 2: Legacy fallback (pgrep/tasklist + orphan check) ---
    if sys.platform == "win32":
        try:
            ps_cmd = (
                f'Get-CimInstance Win32_Process -Filter '
                f'"Name like \'%python%\'" | '
                f'Where-Object {{ $_.CommandLine -match "envoy.bridge" -and '
                f'$_.CommandLine -match "--port {port}" -and '
                f'$_.ProcessId -ne {my_pid} }} | '
                f'Select-Object ProcessId, ParentProcessId | '
                f'ForEach-Object {{ "$($_.ProcessId),$($_.ParentProcessId)" }}'
            )
            result = subprocess.run(
                ["powershell", "-NoProfile", "-Command", ps_cmd],
                capture_output=True, text=True, timeout=10,
            )
            for line in result.stdout.strip().split("\n"):
                line = line.strip()
                if not line:
                    continue
                parts = line.split(",")
                if len(parts) < 2 or not parts[0].isdigit():
                    continue
                pid = int(parts[0])
                ppid = int(parts[1]) if parts[1].isdigit() else 0
                # Only kill if parent is dead (orphan)
                if ppid > 1 and is_process_alive(ppid):
                    continue
                try:
                    subprocess.run(
                        ["taskkill", "/F", "/PID", str(pid)],
                        capture_output=True, timeout=5,
                    )
                    log(f"Terminated orphan bridge (PID {pid}, parent {ppid} dead)")
                except (subprocess.TimeoutExpired, OSError):
                    pass
        except (subprocess.TimeoutExpired, FileNotFoundError, OSError):
            pass
        return
    try:
        result = subprocess.run(
            ["pgrep", "-f", "envoy-bridge.py"],
            capture_output=True, text=True, timeout=5,
        )
        if result.returncode != 0:
            return
        for line in result.stdout.strip().split("\n"):
            line = line.strip()
            if not line:
                continue
            try:
                pid = int(line)
            except ValueError:
                continue
            if pid == my_pid:
                continue
            try:
                cmdline_path = f"/proc/{pid}/cmdline"
                if os.path.exists(cmdline_path):
                    with open(cmdline_path, "r") as f:
                        cmdline = f.read()
                else:
                    ps = subprocess.run(
                        ["ps", "-p", str(pid), "-o", "args="],
                        capture_output=True, text=True, timeout=5,
                    )
                    cmdline = ps.stdout.strip()
                if f"--port" in cmdline and str(port) in cmdline:
                    if _is_orphan(pid):
                        os.kill(pid, signal.SIGTERM)
                        log(f"Terminated orphan bridge (PID {pid})")
                    else:
                        log(f"Skipping active peer bridge (PID {pid}, parent alive)")
            except (ProcessLookupError, PermissionError, OSError):
                pass
    except (subprocess.TimeoutExpired, FileNotFoundError, ValueError):
        pass


def start_orphan_watchdog(stdin_probe_fd, config_path):
    """Start a daemon thread that exits when the MCP client's stdin pipe closes.

    Detects session end via POLLHUP on a dup'd stdin fd (macOS/Linux) or
    PeekNamedPipe failure (Windows).  This replaces the old ppid-based
    detection which failed because the VS Code extension host (the actual
    parent) outlives individual Claude Code sessions.
    """
    def watchdog():
        try:
            if sys.platform == "win32":
                import ctypes
                import msvcrt
                handle = msvcrt.get_osfhandle(stdin_probe_fd)
                kernel32 = ctypes.windll.kernel32
                while True:
                    time.sleep(STDIN_POLL_INTERVAL_MS / 1000)
                    avail = ctypes.c_ulong(0)
                    ok = kernel32.PeekNamedPipe(
                        handle, None, 0, None, ctypes.byref(avail), None)
                    if not ok:
                        log("stdin pipe broken (Windows). Session ended. Exiting.")
                        _cleanup_heartbeat(config_path)
                        os._exit(0)
            else:
                import select as _select
                poller = _select.poll()
                poller.register(stdin_probe_fd,
                                _select.POLLIN | _select.POLLHUP)
                while True:
                    events = poller.poll(STDIN_POLL_INTERVAL_MS)
                    for fd, event in events:
                        if event & (_select.POLLHUP | _select.POLLERR
                                    | _select.POLLNVAL):
                            log("stdin pipe closed (POLLHUP). "
                                "Session ended. Exiting.")
                            _cleanup_heartbeat(config_path)
                            os._exit(0)
        except Exception as e:
            log(f"Orphan watchdog error: {type(e).__name__}: {e}")
            # Never let the watchdog die silently -- loop and retry
            time.sleep(STDIN_POLL_INTERVAL_MS / 1000)
            watchdog()

    t = threading.Thread(target=watchdog, daemon=True, name="orphan-watchdog")
    t.start()


def wait_for_envoy(url, deadline):
    """Block until Envoy is reachable or deadline expires. Returns True on success."""
    attempt = 0
    while time.monotonic() < deadline:
        try:
            # Probe with an empty POST -- any HTTP response (even 4xx/5xx)
            # means the server is up.  Only connection errors (refused,
            # timeout, DNS) indicate it's not running yet.
            urllib.request.urlopen(
                urllib.request.Request(
                    url, data=b"{}",
                    headers={
                        "Content-Type": "application/json",
                        "Accept": "application/json, text/event-stream",
                    },
                ),
                timeout=3,
            )
            return True
        except urllib.error.HTTPError:
            # Server responded (maybe 400/405) -- it's reachable
            return True
        except (urllib.error.URLError, ConnectionError, OSError):
            # Connection refused / timeout -- server not up yet
            pass
        idx = min(attempt, len(RETRY_INTERVALS) - 1)
        wait = RETRY_INTERVALS[idx]
        remaining = deadline - time.monotonic()
        if remaining <= 0:
            break
        actual_wait = min(wait, remaining)
        elapsed = CONNECT_TIMEOUT_S - remaining
        log(f"Waiting for Envoy ({elapsed:.0f}s elapsed, retry in {actual_wait:.1f}s). "
            f"Ensure TouchDesigner is running with Envoy enabled.")
        time.sleep(actual_wait)
        attempt += 1
    return False


