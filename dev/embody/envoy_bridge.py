def forward_to_http(url, message, timeout=300):
    """Forward a JSON-RPC message to the Envoy HTTP endpoint.

    Returns the parsed JSON-RPC response, or None for notifications
    that return no data.
    """
    data = json.dumps(message).encode("utf-8")
    req = urllib.request.Request(
        url,
        data=data,
        headers={
            "Content-Type": "application/json",
            "Accept": "application/json, text/event-stream",
        },
    )
    resp = urllib.request.urlopen(req, timeout=timeout)
    body = resp.read().decode("utf-8")

    # Streamable HTTP returns SSE format: "event: message\ndata: {...}\n\n"
    for line in body.split("\n"):
        stripped = line.strip()
        if stripped.startswith("data: "):
            try:
                return json.loads(stripped[6:])
            except json.JSONDecodeError:
                continue  # Truncated SSE line — try next

    # Fallback: plain JSON response
    if body.strip():
        try:
            return json.loads(body)
        except json.JSONDecodeError:
            pass

    return None


def send_response(response):
    """Write a JSON-RPC response to stdout (STDIO MCP transport)."""
    sys.stdout.write(json.dumps(response) + "\n")
    sys.stdout.flush()


def send_error(request_id, code, message):
    """Send a JSON-RPC error response."""
    send_response({
        "jsonrpc": "2.0",
        "id": request_id,
        "error": {"code": code, "message": message},
    })


def kill_stale_bridges(port):
    """Find and terminate other envoy-bridge processes targeting the same port.

    Claude Code spawns a new bridge process for each session but doesn't
    always clean up old ones when sessions end.  This prevents dozens of
    zombie bridges from accumulating and fighting over the port.
    """
    my_pid = os.getpid()
    if sys.platform == "win32":
        # Windows: use tasklist/wmic — not worth the complexity for now
        return
    try:
        # Find all envoy-bridge processes via pgrep
        result = subprocess.run(
            ["pgrep", "-f", "envoy-bridge.py"],
            capture_output=True, text=True, timeout=5,
        )
        if result.returncode != 0:
            return
        for line in result.stdout.strip().split("\n"):
            pid = int(line.strip())
            if pid == my_pid:
                continue
            # Verify this process is actually targeting our port by reading
            # its command line.  This avoids killing bridges for other ports.
            try:
                cmdline_path = f"/proc/{pid}/cmdline"
                if os.path.exists(cmdline_path):
                    # Linux
                    with open(cmdline_path, "r") as f:
                        cmdline = f.read()
                else:
                    # macOS: use ps
                    ps = subprocess.run(
                        ["ps", "-p", str(pid), "-o", "args="],
                        capture_output=True, text=True, timeout=5,
                    )
                    cmdline = ps.stdout.strip()
                if f"--port" in cmdline and str(port) in cmdline:
                    os.kill(pid, signal.SIGTERM)
                    log(f"Terminated stale bridge process (PID {pid})")
            except (ProcessLookupError, PermissionError, OSError):
                pass  # Already gone or not ours
    except (subprocess.TimeoutExpired, FileNotFoundError, ValueError):
        pass  # pgrep not available or other issue — not critical


def start_orphan_watchdog():
    """Start a daemon thread that exits the process if our parent dies.

    When Claude Code exits without cleanly closing stdin, the bridge
    becomes an orphan process.  On Unix, orphaned processes get reparented
    to PID 1 (init/launchd).  We detect this and self-terminate.
    """
    if sys.platform == "win32":
        return  # ppid semantics differ on Windows
    parent_pid = os.getppid()

    def watchdog():
        while True:
            time.sleep(ORPHAN_CHECK_INTERVAL_S)
            current_ppid = os.getppid()
            if current_ppid != parent_pid:
                # Parent changed (reparented to init/launchd) — we're orphaned
                log(f"Parent process died (was {parent_pid}, now {current_ppid}). Exiting.")
                os._exit(0)

    t = threading.Thread(target=watchdog, daemon=True)
    t.start()


def wait_for_envoy(url, deadline):
    """Block until Envoy is reachable or deadline expires. Returns True on success."""
    attempt = 0
    while time.monotonic() < deadline:
        try:
            # Probe with an empty POST — any HTTP response (even 4xx/5xx)
            # means the server is up.  Only connection errors (refused,
            # timeout, DNS) indicate it's not running yet.
            urllib.request.urlopen(
                urllib.request.Request(
                    url, data=b"{}",
                    headers={
                        "Content-Type": "application/json",
                        "Accept": "application/json, text/event-stream",
                    },
                ),
                timeout=3,
            )
            return True
        except urllib.error.HTTPError:
            # Server responded (maybe 400/405) — it's reachable
            return True
        except (urllib.error.URLError, ConnectionError, OSError):
            # Connection refused / timeout — server not up yet
            pass
        idx = min(attempt, len(RETRY_INTERVALS) - 1)
        wait = RETRY_INTERVALS[idx]
        remaining = deadline - time.monotonic()
        if remaining <= 0:
            break
        actual_wait = min(wait, remaining)
        elapsed = CONNECT_TIMEOUT_S - remaining
        log(f"Waiting for Envoy ({elapsed:.0f}s elapsed, retry in {actual_wait:.1f}s). "
            f"Ensure TouchDesigner is running with Envoy enabled.")
        time.sleep(actual_wait)
        attempt += 1
    return False


