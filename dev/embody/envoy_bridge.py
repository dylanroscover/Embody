# ---------------------------------------------------------------------------
# Shared state + stdout serialization
# ---------------------------------------------------------------------------

# A single lock serializes ALL stdout writes from the main thread
# (send_response) AND the reconciler thread (notify_stdout).  Stdout discipline
# is critical for the stdio MCP transport -- exactly one JSON object per newline,
# no concurrent partial writes.
_stdout_lock = threading.Lock()


class BridgeState:
    """Thread-safe container for all bridge state.

    Access is guarded by an RLock exposed via ``with state:``.  All handlers
    and the reconciler thread touch state through this context manager.
    The same lock serializes mutations to ``config``, ``url``, connection
    flags, PID tracking, tool cache, and launch timestamps.
    """

    def __init__(self, *, url, td_pid=None, config=None,
                 config_path=None, active_name=None):
        self._lock = threading.RLock()
        # Connection state
        self.connected = False
        self.url = url
        self.td_pid = td_pid
        self.last_connected_time = None
        self.last_heartbeat_ok = 0
        self.crash_detected = False
        # Config / registry
        self.config = config or {}
        self.config_path = config_path
        self.config_mtime = 0
        self.active_name = active_name
        # Launch tracking
        self.launch_timestamps = []
        # Tool list cache (populated by main-thread forwards and reconciler)
        self.cached_tools = None
        self.cached_tools_hash = None
        # Proactive PID discovery tracking (populated by reconciler phase 2)
        self.known_td_pids = set()

    def __enter__(self):
        self._lock.acquire()
        return self

    def __exit__(self, exc_type, exc_val, exc_tb):
        self._lock.release()
        return False


def notify_stdout(method, params=None):
    """Send a JSON-RPC notification to the MCP client.

    Serialized via ``_stdout_lock`` so it cannot interleave with a response
    write from the main thread.  Notifications have no ``id`` field.
    """
    msg = {"jsonrpc": "2.0", "method": method}
    if params is not None:
        msg["params"] = params
    with _stdout_lock:
        try:
            sys.stdout.write(json.dumps(msg) + "\n")
            sys.stdout.flush()
        except (BrokenPipeError, OSError):
            # Client closed stdout -- nothing we can do here.  Let the
            # main stdin loop discover the closure on its next iteration.
            pass


