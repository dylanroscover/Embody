def _hash_tools(tools):
    """Return a stable SHA-1 hash of a tools/list payload.

    The hash is order-independent: the tools are sorted by name before
    serialization, so reordering yields the same hash, but adding,
    removing, or renaming a tool yields a different hash.
    """
    if not tools:
        return hashlib.sha1(b"[]").hexdigest()
    try:
        payload = sorted(
            ({"n": t.get("name", ""), "d": t.get("description", "")}
             for t in tools),
            key=lambda item: item["n"],
        )
    except (TypeError, AttributeError):
        return hashlib.sha1(b"[]").hexdigest()
    blob = json.dumps(payload, sort_keys=True, separators=(",", ":"))
    return hashlib.sha1(blob.encode("utf-8")).hexdigest()


def fetch_tools_list(url, timeout=FETCH_TOOLS_TIMEOUT_S):
    """One-shot forward of a ``tools/list`` JSON-RPC request.

    Returns a list of tool dicts on success, or ``None`` on any failure
    (network error, non-OK HTTP, malformed JSON-RPC, unexpected shape).
    Used by the reconciler to refresh the cached tool list after a
    backend transition.
    """
    msg = {"jsonrpc": "2.0", "id": "bridge-tools-list", "method": "tools/list"}
    try:
        resp = forward_to_http(url, msg, timeout=timeout)
    except Exception:
        return None
    if not isinstance(resp, dict):
        return None
    result = resp.get("result")
    if not isinstance(result, dict):
        return None
    tools = result.get("tools")
    if not isinstance(tools, list):
        return None
    return tools


def ping_backend_mcp(url, timeout=BACKEND_PING_TIMEOUT_S):
    """Ping the backend via MCP ``ping`` method.

    Returns True if the backend returned any JSON-RPC response (including
    ``-32601 method not found``, which still indicates the server is up),
    False on any network error.
    """
    if not url:
        return False
    msg = {"jsonrpc": "2.0", "id": "bridge-ping", "method": "ping"}
    try:
        resp = forward_to_http(url, msg, timeout=timeout)
    except Exception:
        return False
    return isinstance(resp, dict) and ("result" in resp or "error" in resp)


def reconcile(state, on_tools_change, *, heartbeat):
    """Run one reconciliation pass against ``state``.

    Phase 1 (every tick, UNCONDITIONAL): re-read ``envoy.json`` when its
    mtime changes, compare the resolved active port against ``state.url``,
    and switch URL immediately on any drift -- regardless of whether the
    current connection still appears healthy.  This is the fix for the
    dominant failure mode where a user opens a new TD instance mid-session
    and the bridge silently stays pinned to the old one.

    Phase 2 (every heartbeat tick, or immediately after a URL switch):
    ping the backend to detect connection state transitions, then on a
    "became connected" transition refresh the cached tool list.  Emits
    ``on_tools_change`` on tool-list hash changes and on disconnect
    transitions (so the client refetches the bridge-only fallback list).
    """
    # --- PHASE 1: Config reconciliation (every tick, unconditional) ---
    with state:
        config_path = state.config_path
        old_mtime = state.config_mtime
        old_url = state.url

    new_url = None
    if config_path and os.path.exists(config_path):
        try:
            mtime = os.path.getmtime(config_path)
        except OSError:
            mtime = old_mtime
        if mtime != old_mtime:
            new_config = load_config(config_path)
            port, pid, active_name = _resolve_from_registry(new_config, None)
            candidate = f"http://localhost:{port}/mcp" if port else None
            with state:
                state.config = new_config
                state.config_mtime = mtime
                if pid:
                    state.td_pid = pid
                if active_name != state.active_name:
                    state.active_name = active_name
            if candidate and candidate != old_url:
                new_url = candidate

    # If the active instance changed, switch URL immediately.
    # This is the unconditional switch -- doesn't matter if the old URL
    # still responds.  Forces a re-probe on this same tick.
    url_switched = False
    if new_url is not None:
        with state:
            log(f"Active instance changed: {old_url} -> {new_url}")
            state.url = new_url
            state.connected = False
            state.cached_tools = None
            state.cached_tools_hash = None
            url_switched = True

    # --- PHASE 2: Heartbeat (every N config ticks per the dynamic cadence
    # in _current_heartbeat_interval_s, or right after a URL switch so the
    # new backend gets probed immediately) ---
    if not heartbeat and not url_switched:
        return

    with state:
        url = state.url
        was_connected = state.connected
        td_pid = state.td_pid

    is_up = ping_backend_mcp(url) if url else False
    pid_alive = is_process_alive(td_pid) if td_pid else False

    became_connected = (not was_connected) and is_up
    became_disconnected = was_connected and not is_up

    with state:
        state.connected = is_up
        if is_up:
            state.last_heartbeat_ok = time.time()
            state.last_connected_time = time.time()
        if not pid_alive and td_pid:
            state.crash_detected = True

    # --- Proactive TD process discovery ---
    # Detect new TD processes that appeared since the last heartbeat.
    # If new TDs are found, force a config re-read on the next tick so
    # the reconciler picks up any registry changes they may have written.
    current_td_pids = set(find_all_td_pids())
    with state:
        old_pids = state.known_td_pids
        new_pids = current_td_pids - old_pids
        gone_pids = old_pids - current_td_pids
        state.known_td_pids = current_td_pids
        if new_pids:
            state.config_mtime = 0  # Force config re-read next tick
    if new_pids:
        log(f"New TD process(es) detected: {sorted(new_pids)}")
    if gone_pids:
        log(f"TD process(es) exited: {sorted(gone_pids)}")

    notify_changed = False

    if became_connected:
        # Refresh the tool list on a connect transition.  If the hash
        # changed (different Envoy version or tool set), fire list_changed.
        # If identical, this is a silent switch -- no notification.
        new_tools = fetch_tools_list(url)
        if new_tools is not None:
            augmented = list(new_tools) + list(BRIDGE_TOOLS)
            new_hash = _hash_tools(augmented)
            with state:
                old_hash = state.cached_tools_hash
                state.cached_tools = augmented
                state.cached_tools_hash = new_hash
                cp = state.config_path
            save_tools_cache(cp, augmented)
            if old_hash is None or old_hash != new_hash:
                notify_changed = True
            else:
                log("Backend switched but tool list is unchanged; no notify")

    if became_disconnected:
        # On disconnect, invalidate the cache and tell the client to
        # refetch -- they'll get the bridge-only fallback list.
        with state:
            state.cached_tools = None
            state.cached_tools_hash = None
        notify_changed = True

    if notify_changed and on_tools_change is not None:
        try:
            on_tools_change()
        except Exception as e:
            log(f"on_tools_change callback failed: {type(e).__name__}: {e}")


def _current_heartbeat_interval_s(state):
    """Return the fixed heartbeat interval (seconds)."""
    return HEARTBEAT_TICK_S


def start_reconciler(state, on_tools_change):
    """Start the daemon reconciler thread.

    The thread loops forever at ``CONFIG_TICK_S`` resolution, running
    the cheap config-mtime check every tick (Phase 1) and the backend
    heartbeat every ``HEARTBEAT_TICK_S`` seconds (Phase 2).  All
    exceptions inside the loop body are caught and logged -- the thread
    must never die.
    """
    def loop():
        # Force first heartbeat on tick 0 (i.e. after the initial sleep)
        last_heartbeat_at_tick = -10 ** 9
        tick = 0
        while True:
            time.sleep(CONFIG_TICK_S)
            try:
                interval_s = _current_heartbeat_interval_s(state)
                ticks_per_heartbeat = max(1, int(interval_s // CONFIG_TICK_S))
                do_heartbeat = (tick - last_heartbeat_at_tick) >= ticks_per_heartbeat
                reconcile(state, on_tools_change, heartbeat=do_heartbeat)
                if do_heartbeat:
                    last_heartbeat_at_tick = tick
                # Touch heartbeat on every tick so peers know we're alive
                _touch_heartbeat(state.config_path)
            except Exception as e:
                # NEVER let an exception kill the reconciler -- log and continue
                log(f"Reconciler error (continuing): "
                    f"{type(e).__name__}: {e}")
            tick += 1

    t = threading.Thread(target=loop, daemon=True, name="envoy-reconciler")
    t.start()
    return t


