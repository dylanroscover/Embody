def _is_bridge_process(pid):
    """Check if a PID is an envoy-bridge process (not actual TouchDesigner).

    The bridge runs TD's bundled Python, so pgrep -f TouchDesigner matches
    it.  This helper reads the process cmdline and returns True if it
    contains 'envoy-bridge' or 'envoy_bridge'.
    """
    try:
        cmdline_path = f"/proc/{pid}/cmdline"
        if os.path.exists(cmdline_path):
            with open(cmdline_path, "r") as f:
                cmdline = f.read()
        else:
            # macOS
            result = subprocess.run(
                ["ps", "-p", str(pid), "-o", "args="],
                capture_output=True, text=True, timeout=5,
            )
            cmdline = result.stdout
        return "envoy-bridge" in cmdline or "envoy_bridge" in cmdline
    except (OSError, subprocess.TimeoutExpired, FileNotFoundError):
        return False


def find_all_td_pids():
    """Return a list of all running TouchDesigner PIDs.

    Excludes the bridge's own PID and any other envoy-bridge processes,
    which run TD's bundled Python and would otherwise false-match.
    """
    pids = []
    my_pid = os.getpid()
    if sys.platform == "win32":
        try:
            result = subprocess.run(
                ["tasklist", "/FI", "IMAGENAME eq TouchDesigner*",
                 "/FO", "CSV", "/NH"],
                capture_output=True, text=True, timeout=5,
            )
            for line in result.stdout.strip().split("\n"):
                if "TouchDesigner" in line:
                    parts = line.strip('"').split('","')
                    if len(parts) >= 2:
                        try:
                            pids.append(int(parts[1]))
                        except ValueError:
                            pass
        except (subprocess.TimeoutExpired, FileNotFoundError, ValueError):
            pass
        # Windows tasklist filters by image name (TouchDesigner*.exe),
        # so bridge processes (python.exe) won't appear.
        return pids

    # macOS / Linux -- pgrep -f matches cmdline, which catches bridges
    # running TD's bundled Python.  Filter them out.
    try:
        result = subprocess.run(
            ["pgrep", "-f", "TouchDesigner"],
            capture_output=True, text=True, timeout=5,
        )
        if result.returncode == 0:
            for line in result.stdout.strip().split("\n"):
                line = line.strip()
                if not line:
                    continue
                try:
                    pid = int(line)
                except ValueError:
                    continue
                if pid == my_pid:
                    continue
                if _is_bridge_process(pid):
                    continue
                pids.append(pid)
    except (subprocess.TimeoutExpired, FileNotFoundError, ValueError):
        pass
    return pids


def find_td_pid():
    """Find the PID of a running TouchDesigner process. Returns int or None."""
    pids = find_all_td_pids()
    return pids[0] if pids else None


def is_process_alive(pid):
    """Check if a process is still running."""
    if not pid or pid <= 0:
        return False
    if sys.platform == "win32":
        # os.kill(pid, 0) on Windows calls TerminateProcess() -- it KILLS the
        # process instead of checking liveness.  Use OpenProcess(SYNCHRONIZE)
        # which only requires the right to wait on the object.
        try:
            import ctypes
            kernel32 = ctypes.windll.kernel32
            SYNCHRONIZE = 0x00100000
            handle = kernel32.OpenProcess(SYNCHRONIZE, False, pid)
            if handle:
                kernel32.CloseHandle(handle)
                return True
            return False
        except Exception:
            return False
    # Unix: signal 0 is a no-op liveness check
    try:
        os.kill(pid, 0)
        return True
    except (ProcessLookupError, PermissionError):
        return False
    except OSError:
        return False


def ping_envoy_port(port):
    """Check if an Envoy server is listening on the given port.
    Returns True if something responds, False otherwise."""
    if port is None:
        return False
    import socket
    try:
        with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
            s.settimeout(1)
            return s.connect_ex(('127.0.0.1', int(port))) == 0
    except (OSError, ValueError):
        return False


def get_instance_status(config):
    """Read the instance registry from config and check reachability.
    Returns (instances_with_status: dict, active_name: str|None,
    unregistered_td_pids: list[int])."""
    instances = config.get("instances", {})
    active = config.get("active")
    result = {}
    registered_pids = set()
    for name, info in instances.items():
        pid = info.get("td_pid")
        if pid:
            registered_pids.add(pid)
        pid_alive = is_process_alive(pid)
        port_open = ping_envoy_port(info.get("port"))
        # An instance is only reachable if its own PID is alive AND its port
        # responds.  A dead PID with an open port means a different instance
        # owns that port -- this entry is stale.
        reachable = pid_alive and port_open
        result[name] = {
            "toe_path": info.get("toe_path", ""),
            "port": info.get("port"),
            "td_pid": pid,
            "reachable": reachable,
            "active": name == active,
        }
    # Discover TD processes not in the registry
    all_td_pids = find_all_td_pids()
    unregistered = [p for p in all_td_pids if p not in registered_pids]
    return result, active, unregistered


def atomic_write_json(path, data):
    """Write JSON atomically via temp file + os.replace().
    Retries on PermissionError (Windows file-in-use).
    Uses PID-tagged temp file so concurrent bridges don't collide."""
    tmp = path + f".{os.getpid()}.tmp"
    content = json.dumps(data, indent=2) + "\n"
    for attempt in range(3):
        try:
            with open(tmp, "w", encoding="utf-8") as f:
                f.write(content)
            os.replace(tmp, path)
            return
        except PermissionError:
            if attempt < 2:
                time.sleep(0.1)
            else:
                raise


def _heartbeat_path(config_path, pid=None):
    """Return the path for this bridge's heartbeat file.

    Places it in dev/logs/ relative to the git root.  Falls back to a
    temp dir if the log directory doesn't exist.
    """
    if pid is None:
        pid = os.getpid()
    filename = f"envoy-bridge-{pid}.heartbeat"
    if config_path:
        # Config is in .embody/ — git root is one level up
        embody_dir = os.path.dirname(os.path.abspath(config_path))
        git_root = os.path.dirname(embody_dir)
        log_dir = os.path.join(git_root, "dev", "logs")
        if os.path.isdir(log_dir):
            return os.path.join(log_dir, filename)
    import tempfile
    return os.path.join(tempfile.gettempdir(), filename)


def _touch_heartbeat(config_path):
    """Write PID + timestamp to our heartbeat file.  Best-effort, never raises."""
    try:
        path = _heartbeat_path(config_path)
        atomic_write_json(path, {"pid": os.getpid(), "time": time.time()})
    except Exception:
        pass


def _cleanup_heartbeat(config_path):
    """Remove our heartbeat file.  Best-effort, never raises."""
    try:
        path = _heartbeat_path(config_path)
        if os.path.exists(path):
            os.remove(path)
    except Exception:
        pass


def _list_stale_heartbeats(config_path, max_age_s):
    """Glob for heartbeat files and return [(pid, age)] for stale ones.

    Also removes heartbeat files for PIDs that are no longer alive.
    """
    import glob as _glob
    stale = []
    if config_path:
        # Config is in .embody/ — git root is one level up
        embody_dir = os.path.dirname(os.path.abspath(config_path))
        git_root = os.path.dirname(embody_dir)
        log_dir = os.path.join(git_root, "dev", "logs")
    else:
        import tempfile
        log_dir = tempfile.gettempdir()
    if not os.path.isdir(log_dir):
        return stale
    pattern = os.path.join(log_dir, "envoy-bridge-*.heartbeat")
    now = time.time()
    my_pid = os.getpid()
    for path in _glob.glob(pattern):
        try:
            with open(path, "r", encoding="utf-8") as f:
                data = json.load(f)
            pid = data.get("pid")
            ts = data.get("time", 0)
            if pid is None:
                continue
            if pid == my_pid:
                continue
            # If the process is dead, clean up its heartbeat file
            if not is_process_alive(pid):
                try:
                    os.remove(path)
                except OSError:
                    pass
                continue
            age = now - ts
            if age > max_age_s:
                stale.append((pid, age))
        except (json.JSONDecodeError, OSError, KeyError, TypeError):
            continue
    return stale


def quit_td(pid, graceful_timeout=15):
    """Quit a running TouchDesigner process.

    Sends a graceful termination first, then force-kills if needed.
    Returns (success: bool, message: str).
    """
    if pid is None:
        return False, "No TouchDesigner PID to quit"
    if not is_process_alive(pid):
        return True, "TouchDesigner already exited"

    # Graceful quit
    try:
        if sys.platform == "darwin":
            subprocess.run(
                ["osascript", "-e", 'quit app "TouchDesigner"'],
                capture_output=True, timeout=10,
            )
        elif sys.platform == "win32":
            subprocess.run(
                ["taskkill", "/PID", str(pid)],
                capture_output=True, timeout=10,
            )
        else:
            os.kill(pid, signal.SIGTERM)
    except (subprocess.TimeoutExpired, OSError) as e:
        log(f"Graceful quit failed: {e}")

    # Wait for exit
    deadline = time.monotonic() + graceful_timeout
    while time.monotonic() < deadline:
        if not is_process_alive(pid):
            return True, f"TouchDesigner (PID {pid}) exited gracefully"
        time.sleep(0.5)

    # Force kill
    log(f"TouchDesigner (PID {pid}) did not exit gracefully, force killing")
    try:
        if sys.platform == "win32":
            subprocess.run(
                ["taskkill", "/F", "/PID", str(pid)],
                capture_output=True, timeout=10,
            )
        else:
            os.kill(pid, signal.SIGKILL)
    except (subprocess.TimeoutExpired, OSError) as e:
        return False, f"Failed to kill TouchDesigner (PID {pid}): {e}"

    # Brief wait after force kill
    time.sleep(1)
    if is_process_alive(pid):
        return False, f"TouchDesigner (PID {pid}) could not be terminated"

    return True, f"TouchDesigner (PID {pid}) was force-killed"


def launch_td(config, config_path, project_path=None):
    """Launch TouchDesigner with a .toe file.

    Args:
        config: Parsed envoy.json config dict.
        config_path: Path to envoy.json (for resolving relative paths).
        project_path: Optional override .toe path. If relative, resolved
            against the git root (same directory as config_path).

    Returns (success: bool, message: str, pid: int|None).
    """
    td_exe = config.get("td_executable")
    if not td_exe:
        return False, "No td_executable configured in envoy.json", None

    if project_path:
        # Resolve relative paths against git root
        if not os.path.isabs(project_path) and config_path:
            embody_dir = os.path.dirname(os.path.abspath(config_path))
            git_root = os.path.dirname(embody_dir)
            project_path = os.path.join(git_root, project_path)
        toe_path = project_path
    else:
        toe_path = resolve_toe_path(config, config_path)
    if not toe_path:
        return False, "No toe_path configured in envoy.json", None

    # Validate paths
    if sys.platform == "darwin":
        # On macOS, td_executable is the .app bundle path
        if not os.path.exists(td_exe):
            return (False,
                    f"TouchDesigner not found at {td_exe}. "
                    "Install this version or update td_executable in envoy.json",
                    None)
    else:
        if not os.path.isfile(td_exe):
            return (False,
                    f"TouchDesigner executable not found at {td_exe}. "
                    "Install this version or update td_executable in envoy.json",
                    None)

    if not os.path.isfile(toe_path):
        return (False,
                f"Project file not found: {toe_path}",
                None)

    # Launch TD
    log(f"Launching TouchDesigner: {td_exe} with {toe_path}")
    try:
        if sys.platform == "darwin":
            proc = subprocess.Popen(
                ["open", "-a", td_exe, toe_path],
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
            )
            # 'open' returns immediately; TD process is spawned by LaunchServices.
            # Wait briefly for open to finish, then find TD's actual PID.
            proc.wait(timeout=10)
            time.sleep(2)  # Give TD a moment to start
            pid = find_td_pid()
        elif sys.platform == "win32":
            proc = subprocess.Popen(
                [td_exe, toe_path],
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
            )
            pid = proc.pid
        else:
            proc = subprocess.Popen(
                [td_exe, toe_path],
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
            )
            pid = proc.pid

        return True, f"TouchDesigner launched (PID {pid})", pid

    except Exception as e:
        return False, f"Failed to launch TouchDesigner: {e}", None


