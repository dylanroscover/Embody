def log(msg):
    """Log to stderr (visible in Claude Code's MCP server output)."""
    sys.stderr.write(f"[envoy-bridge] {msg}\n")
    sys.stderr.flush()


def parse_args():
    port = DEFAULT_PORT
    config_path = None
    args = sys.argv[1:]
    for i, arg in enumerate(args):
        if arg == "--port" and i + 1 < len(args):
            port = int(args[i + 1])
        elif arg == "--config" and i + 1 < len(args):
            config_path = args[i + 1]
    return port, config_path


def load_config(config_path):
    """Load .envoy.json config. Returns dict (possibly empty)."""
    if not config_path or not os.path.exists(config_path):
        return {}
    try:
        with open(config_path, "r", encoding="utf-8") as f:
            return json.load(f)
    except (json.JSONDecodeError, OSError) as e:
        log(f"Warning: could not read config {config_path}: {e}")
        return {}


def resolve_toe_path(config, config_path):
    """Resolve the .toe path from config (relative to git root)."""
    toe = config.get("toe_path")
    if not toe:
        return None
    if os.path.isabs(toe):
        return toe
    # Config sits at git root — resolve relative to its directory
    if config_path:
        git_root = os.path.dirname(os.path.abspath(config_path))
        return os.path.join(git_root, toe)
    return toe


