def _resolve_from_registry(config, fallback_port):
    """Resolve port and PID from the instance registry.
    Returns (port, td_pid, active_name)."""
    instances = config.get("instances", {})
    active_name = config.get("active")

    if active_name and active_name in instances:
        active = instances[active_name]
        port = active.get("port", fallback_port)
        config_pid = active.get("td_pid")
        if config_pid and is_process_alive(config_pid):
            return port, config_pid, active_name
        return port, find_td_pid(), active_name

    # Old-format config or empty registry -- use fallback
    if "port" in config and "instances" not in config:
        port = config.get("port", fallback_port)
        config_pid = config.get("td_pid")
        if config_pid and is_process_alive(config_pid):
            return port, config_pid, None
        return port, find_td_pid(), None

    return fallback_port, find_td_pid(), None


# Module-level config path for signal handler cleanup
_config_path_for_cleanup = None


def _install_signal_diagnostics():
    """Install signal handlers that log before exiting, to diagnose what kills the bridge."""
    my_pid = os.getpid()
    ppid = os.getppid()

    def _handle_sigterm(signum, frame):
        log(f"Received SIGTERM (PID {my_pid}, parent {os.getppid()}, "
            f"original parent {ppid})")
        _cleanup_heartbeat(_config_path_for_cleanup)
        sys.exit(0)

    def _handle_sigint(signum, frame):
        log(f"Received SIGINT (PID {my_pid}, parent {os.getppid()}, "
            f"original parent {ppid})")
        raise KeyboardInterrupt

    signal.signal(signal.SIGTERM, _handle_sigterm)
    signal.signal(signal.SIGINT, _handle_sigint)


def main():
    cli_port, config_path = parse_args()
    _init_file_logging(config_path)

    config = load_config(config_path)
    port, td_pid, active_name = _resolve_from_registry(config, cli_port)
    url = f"http://localhost:{port}/mcp"

    # Seed config_mtime so the reconciler only reacts to actual drift,
    # not the initial load we already performed here.
    initial_mtime = 0
    if config_path and os.path.exists(config_path):
        try:
            initial_mtime = os.path.getmtime(config_path)
        except OSError:
            initial_mtime = 0

    # Bridge state -- shared with meta-tool handlers and the reconciler
    # thread.  All access goes through ``with state:`` (RLock).
    state = BridgeState(
        url=url,
        td_pid=td_pid,
        config=config,
        config_path=config_path,
        active_name=active_name,
    )
    with state:
        state.config_mtime = initial_mtime

    my_pid = os.getpid()
    ppid = os.getppid()
    if active_name:
        log(f"Starting (instance: {active_name}, port: {port}) "
            f"[PID {my_pid}, parent {ppid}]")
    else:
        log(f"Starting (target: localhost:{port}) "
            f"[PID {my_pid}, parent {ppid}]")
    if config_path:
        log(f"Config: {config_path}")
        td_exe = config.get("td_executable", "")
        if td_exe and not os.path.exists(td_exe):
            log(f"WARNING: TouchDesigner not found at {td_exe}")

    log(f"Python: {sys.executable} ({sys.version.split()[0]})")

    # Dup stdin fd BEFORE any threads start -- the watchdog polls this for
    # POLLHUP to detect session end without stealing bytes from the main loop.
    stdin_probe_fd = os.dup(0)

    # Write initial heartbeat and register cleanup
    global _config_path_for_cleanup
    _config_path_for_cleanup = config_path
    _touch_heartbeat(config_path)
    import atexit
    atexit.register(_cleanup_heartbeat, config_path)

    # Install signal handlers AFTER logging init so we can see what kills us
    _install_signal_diagnostics()

    # Clean up stale bridge processes from previous Claude Code sessions
    kill_stale_bridges(port, config_path)

    # Self-terminate when MCP client closes stdin pipe (session ended)
    start_orphan_watchdog(stdin_probe_fd, config_path)

    # Start the reconciler thread -- polls envoy.json every second,
    # pings the backend on a dynamic cadence (fast while unstable,
    # slow once the link has been stable for STABILITY_THRESHOLD_S),
    # switches URL on any active-instance drift.  This is the core v2
    # fix for the open-a-new-TD-instance-mid-session failure mode.
    start_reconciler(
        state,
        # NOTE: Claude Code ignores this (bug #13646); kept for future-proofing.
        lambda: notify_stdout("notifications/tools/list_changed"),
    )

    with state:
        if state.td_pid:
            log(f"Found TouchDesigner process (PID {state.td_pid})")

    for line in sys.stdin:
        line = line.strip()
        if not line:
            continue

        try:
            message = json.loads(line)
        except json.JSONDecodeError:
            log(f"Ignoring malformed JSON: {line[:100]}")
            continue

        is_notification = "id" not in message
        request_id = message.get("id")
        method = message.get("method", "")
        params = message.get("params", {})

        # --- Intercept bridge meta-tool calls ---
        if method == "tools/call" and params.get("name") in BRIDGE_TOOL_NAMES:
            tool_name = params["name"]
            tool_args = params.get("arguments", {})
            log(f"Handling bridge tool: {tool_name}")
            content = handle_bridge_tool(tool_name, tool_args, state)
            if not is_notification:
                send_response({
                    "jsonrpc": "2.0",
                    "id": request_id,
                    "result": {"content": content},
                })
            continue

        # --- Local ping handler (zero latency, no backend dependency) ---
        if method == "ping":
            if not is_notification:
                send_response({"jsonrpc": "2.0", "id": request_id, "result": {}})
            continue

        # --- Pull current URL/connected flags for this iteration ---
        # The reconciler thread can mutate these concurrently.
        with state:
            current_url = state.url
            is_connected = state.connected

        # --- MCP protocol messages handled locally when Envoy is down ---
        # initialize, notifications/initialized, and tools/list must work
        # without Envoy so that bridge meta-tools (launch_td, get_td_status)
        # are available even when TD isn't running.
        if not is_connected:
            if method == "initialize":
                if not is_notification:
                    send_response({
                        "jsonrpc": "2.0",
                        "id": request_id,
                        "result": {
                            "protocolVersion": "2024-11-05",
                            "capabilities": {"tools": {"listChanged": True}},
                            "serverInfo": {
                                "name": "envoy-bridge",
                                "version": "2.0.0",
                            },
                        },
                    })
                continue

            if method == "notifications/initialized":
                continue  # Notification -- no response needed

            if method == "tools/list":
                # Quick probe -- if Envoy is already up, connect and return
                # the full tool list.  If not, return the best available
                # tools (disk cache or bridge-only) immediately.  The
                # reconciler handles recovery in the background.
                deadline = time.monotonic() + INITIAL_PROBE_TIMEOUT_S
                if wait_for_envoy(current_url, deadline):
                    with state:
                        state.connected = True
                        state.last_connected_time = time.time()
                        state.crash_detected = False
                        if not state.td_pid:
                            state.td_pid = find_td_pid()
                    is_connected = True
                    log("Connected to Envoy (during tools/list)")
                    # Fall through to the forwarding path below
                else:
                    log("Envoy not reachable within quick probe -- "
                        "returning cached/bridge-only tools (reconciler will recover)")
                    if not is_notification:
                        send_response(best_available_tools_list(
                            request_id, state.config_path))
                    continue

        # --- Tool list cache hit (connected path) ---
        # If we just answered tools/list recently and the reconciler's
        # last heartbeat confirms the backend is up, serve from cache
        # instead of round-tripping to TD.  Saves a request on repeated
        # tools/list calls during session start.
        if is_connected and method == "tools/list" and not is_notification:
            with state:
                cached = state.cached_tools
                cached_age = time.time() - state.last_heartbeat_ok
            if cached is not None and cached_age < TOOL_CACHE_TTL_S:
                cached_response = {
                    "jsonrpc": "2.0",
                    "id": request_id,
                    "result": {"tools": list(cached)},
                }
                augment_tools_list(cached_response)
                send_response(cached_response)
                continue

        # --- Connection management ---
        # When disconnected, DON'T block waiting for Envoy.  Just update
        # the URL from config (in case the instance changed) and fall
        # through to the forward path.  If the forward succeeds, we mark
        # connected.  If it fails, the error handler marks disconnected
        # and the reconciler drives recovery.  This avoids the 3-second
        # blocking probe that was causing cascading "not responding"
        # errors during normal TD operations.
        if not is_connected:
            with state:
                fresh_config_path = state.config_path
            fresh_config = load_config(fresh_config_path)
            new_port, new_pid, new_active = _resolve_from_registry(
                fresh_config, cli_port)
            new_url = f"http://localhost:{new_port}/mcp" if new_port else current_url
            if new_url != current_url:
                with state:
                    state.url = new_url
                    state.config = fresh_config
                    state.active_name = new_active
                current_url = new_url
                port = new_port
                log(f"Config changed -- now targeting port {port}"
                    + (f" (instance: {new_active})" if new_active else ""))
            if new_pid:
                with state:
                    state.td_pid = new_pid
            # Fall through to the forward path -- no blocking wait.

        # --- Forward to TD (single attempt -- reconciler handles recovery) ---
        response = None
        try:
            response = forward_to_http(current_url, message)
            with state:
                state.last_connected_time = time.time()
        except (urllib.error.URLError, ConnectionError, OSError) as e:
            with state:
                state.connected = False
            msg = connection_lost_message(state)
            log(f"Lost connection to Envoy: {e}")
            if not is_notification:
                send_error(request_id, -32000, msg)
        except (json.JSONDecodeError, ValueError) as e:
            with state:
                state.connected = False
            msg = connection_lost_message(state)
            log(f"Malformed response from Envoy: {e}")
            if not is_notification:
                send_error(request_id, -32000, msg)
        except Exception as e:
            # Catch-all: NEVER let the bridge die from a transient error
            with state:
                state.connected = False
            log(f"Unexpected error (bridge staying alive): {type(e).__name__}: {e}")
            if not is_notification:
                send_error(
                    request_id, -32000,
                    f"Unexpected error ({type(e).__name__}: {e}). Will reconnect on next request.",
                )

        # Augment tools/list responses with bridge meta-tools, cache them
        if response is not None and method == "tools/list":
            augment_tools_list(response)
            # Cache the full tool list (including bridge tools) so future
            # tools/list requests within TOOL_CACHE_TTL_S can be answered
            # from memory.  The reconciler uses hash diffing on a separate
            # cache populated by fetch_tools_list, so this one only serves
            # the main-thread cache-hit path.
            try:
                all_tools = response.get("result", {}).get("tools", [])
                with state:
                    state.cached_tools = list(all_tools)
                    state.cached_tools_hash = _hash_tools(all_tools)
                    state.last_heartbeat_ok = time.time()
                    cp = state.config_path
                save_tools_cache(cp, all_tools)
            except (AttributeError, TypeError):
                pass

        # Send response for requests (not for notifications)
        if response is not None and not is_notification:
            send_response(response)

    log("stdin closed, exiting")


if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        log("Interrupted, exiting")
    except BrokenPipeError:
        pass  # Client closed stdout
